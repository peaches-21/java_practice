import java.util.Collection;

public class WrapperClass {
    /*
     Wrapper classes provide a way to use primitive data types (int, boolean, etc..) as objects
     Sometimes you must use wrapper classes, 
     for example when working with Collection objects, 
     such as ArrayList, where primitive types cannot be used (the list can only store objects)
     ArrayList<int> myNumbers = new ArrayList<int>(); // Invalid
     ArrayList<Integer> myNumbers = new ArrayList<Integer>(); // Valid

     For example, the following methods are used to get the value associated with the corresponding 
     wrapper object: intValue(), byteValue(), shortValue(), longValue(), floatValue(), doubleValue(), 
     charValue(), booleanValue().
     Integer myInt = 5;
     Double myDouble = 5.99;
     Character myChar = 'A';
     System.out.println(myInt.intValue());
     System.out.println(myDouble.doubleValue());
     System.out.println(myChar.charValue());
     Integer myInt = 100;
     String myString = myInt.toString();
    */
}
